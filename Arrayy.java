import java.util.*;
import java.util.ArrayList;

 public class Arrayy{
	public static void main(String[] args){
		int num=0;
		Scanner t=new Scanner(System.in);
		int put=t.nextInt();
		ArrayList<Int> nu= new ArrayList<float>();
		for(int i=0; i<=put; i++){
			nu=num+t.nextInt(i);
		}
		System.out.println(nu);
	}
}